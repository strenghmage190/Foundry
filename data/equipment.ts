/**
 * Sistema de Equipamentos - Beyonders
 * Armas, Armaduras e Munições
 */

export type EquipmentCategory = 'Arma Simples' | 'Arma de Haste' | 'Lâmina Pesada' | 'Esmagadora' | 'Arma Exótica' | 'Arma de Disparo (1 Mão)' | 'Arma de Disparo (2 Mãos)' | 'Armadura' | 'Escudo' | 'Munição';

export interface Equipment {
  id: string;
  name: string;
  category: EquipmentCategory;
  na: number; // Nível de Acesso
  espacos: number; // Espaços no inventário (0, 0.5, 1, 2, 3, 5+)
  descricao: string;
  qualidades: string[]; // Array de qualidades/características
}

export interface Weapon extends Equipment {
  category: 'Arma Simples' | 'Arma de Haste' | 'Lâmina Pesada' | 'Esmagadora' | 'Arma Exótica' | 'Arma de Disparo (1 Mão)' | 'Arma de Disparo (2 Mãos)';
  dano: string; // ex: "1d6", "2d6", "1d8+2"
  tipo: 'Corte' | 'Perfuração' | 'Impacto' | 'C/P' | 'C/I' | 'Bala' | 'Cart.' | 'Virote' | 'Perf.';
  critico: string; // ex: "x2", "19/x2", "18-20"
  alcance?: string; // Para armas de disparo
  municao?: number; // Capacidade de munição
}

export interface Armor extends Equipment {
  category: 'Armadura' | 'Escudo';
  bonusArmadura: number; // Bônus em dados
  penalidades?: string[]; // Penalidades aplicadas
}

export interface Ammunition extends Equipment {
  category: 'Munição';
  compatibilidade: string[]; // Tipos de armas compatíveis
  quantidade: number; // Quantidade na caixa
  efeito: string;
}

